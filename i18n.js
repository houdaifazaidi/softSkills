let currentLang = 'fr';

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('ss_lang', lang);

  const html = document.documentElement;
  html.lang = lang;
  html.dir = lang === 'ar' ? 'rtl' : 'ltr';

  // Apply text translations
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key] !== undefined) {
      el.textContent = translations[lang][key];
    }
  });

  // Apply placeholder translations
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang][key] !== undefined) {
      el.placeholder = translations[lang][key];
    }
  });

  // Update active lang button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Update title
  if (document.querySelector('title[data-i18n]')) {
    const key = document.querySelector('title').getAttribute('data-i18n');
    if (translations[lang][key]) document.title = translations[lang][key];
  }
}

function initLanguage() {
  const saved = localStorage.getItem('ss_lang') || 'fr';
  setLanguage(saved);
}

function t(key) {
  return translations[currentLang][key] || translations['fr'][key] || key;
}
